package com.kkl.kklplus.golden.commonEnum;

import lombok.Getter;
import lombok.Setter;

public enum StatusEnum {

    NEW_REQUEST(10,"新申请"),
    PROCESS(20,"处理中"),
    HONG_UP(25,"挂起"),
    PAY_FAIL(30,"支付失败"),
    PAY_SUCCESS(40,"支付成功");

    @Setter
    @Getter
    private Integer value;

    @Setter
    @Getter
    private String name;

    private StatusEnum(int value, String name) {
        this.value = value;
        this.name = name;
    }

}
